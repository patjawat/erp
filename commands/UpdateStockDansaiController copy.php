    public function actionIndex() 
    {
        $stockList = $this->listStock();
        $stockMap = [];
        foreach ($stockList as $stock) {
            $stockMap[$stock[1]] = [
                'warehouse_id' => $stock[0],
                'asset_item' => str_replace(',', '', $stock[1]),
                'qty' => str_replace(',', '', $stock[2]),
                'unit_price' => str_replace(',', '', $stock[3]),
            ];
        }

        $updated = 0;
        $notFound = 0;

$StockEvents = StockEvent::find()
    // ->select(['asset_item', 'warehouse_id'])
    ->where(['name' => 'order_item'])
    ->groupBy(['asset_item', 'warehouse_id'])
    // ->asArray() // แนะนำใส่เพื่อให้ได้ array แทน ActiveRecord object
    ->all();

        foreach ($StockEvents as $item) {
            if (isset($stockMap[$item->asset_item])) {
                $data = $stockMap[$item->asset_item];
                $item->warehouse_id = $data['warehouse_id'];
                $item->asset_item = $data['asset_item'];
                $item->qty = $data['qty'];
                $item->unit_price = $data['unit_price'];
                $updated++;
                echo "Updated: {$item->asset_item}\n";
            } else {
                $item->qty = 0;
                $item->unit_price = 0;
                $notFound++;
                echo "Not found in stock list: {$item->asset_item}\n";
            }
            $item->order_status = 'pending';
            $item->save(false);
        }

        echo "Update complete. Updated: $updated, Not found: $notFound\n";
    }

